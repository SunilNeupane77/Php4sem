// 11. Write a function that accepts two numbers as parameters and returns their sum.
<?php
// Define the function to sum two numbers
function sum($num1, $num2) {
    return $num1 + $num2;
}

// Call the function with two numbers and display the result
echo "The sum of 5 and 10 is: " . sum(5, 10) . "<br>";
echo "The sum of 20 and 30 is: " . sum(20, 30) . "<br>";
?>
