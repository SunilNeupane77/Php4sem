// Write a script that trims whitespace from the beginning and end of a string using the
trim() function.
<?php
// Define a string with leading and trailing whitespace
$originalString = "   Learning PHP is fun!   ";

// Trim the whitespace
$trimmedString = trim($originalString);

// Display the results
echo "Original String: '$originalString'<br>";
echo "Trimmed String: '$trimmedString'<br>";
?>
